#include<iostream>
using namespace std;

int main()
{
    // Bitwise operator

    // OR operator
    int ans = 2|3;
    cout<<ans<<endl;

    // AND operator
    int x = 2&3;
    cout<<x<<endl;

    // XOR operator
    int ans1 = 2^3;
    cout<<ans1<<endl;

    // left shift
    int ans2 = 2<<3;
    cout<<ans2<<endl;

    // right shift
    int ans3 = 2>>3;
    cout<<ans3<<endl;

    // compliment operator
    int ans4 = ~5;
    cout<<ans4<<endl;
}
